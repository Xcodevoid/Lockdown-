export function t(lang, path, vars) {
  const parts = path.split('.');
  let node = translations[lang];
  for (const p of parts) {
    node = node?.[p];
  }
  if (node == null) return path;
  if (typeof node === 'string' && vars) {
    return node.replace(/\{(\w+)\}/g, (_, key) => vars[key] ?? '');
  }
  return node;
}
